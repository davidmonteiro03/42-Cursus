#include "../include/philo.h"

void	ph_iin(t_in **in, char **v)
{
	(*in)->np = ph_num(v[1]);
	(*in)->td = ph_num(v[2]);
	(*in)->te = ph_num(v[3]);
	(*in)->ts = ph_num(v[4]);
	if (v[5])
		(*in)->ne = ph_num(v[5]);
	else
		(*in)->ne = -1;
}
