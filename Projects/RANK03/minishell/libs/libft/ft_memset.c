#include "libft.h"

void	*ft_memset(void *src,
				int c,
				size_t n)
{
	unsigned char	*mem;

	mem = (unsigned char *)src;
	while (n-- > 0)
		*mem++ = (unsigned char)c;
	return (src);
}
