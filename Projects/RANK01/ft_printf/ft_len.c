#include "ft_printf.h"

size_t	ft_len(size_t n)
{
	size_t	len;

	len = 0;
	if (n <= 0)
		len = 1;
	while (n != 0)
	{
		len++;
		n /= 16;
	}
	return (len + 2);
}
